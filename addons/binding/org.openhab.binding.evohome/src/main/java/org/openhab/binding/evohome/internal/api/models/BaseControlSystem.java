package org.openhab.binding.evohome.internal.api.models;

public abstract class BaseControlSystem implements ControlSystem  {

    private int id;
    private String name;

    public BaseControlSystem(int id, String name) {
        this.id   = id;
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public String getName() {
        return name;
    }

}
